function dv(date) {
    let expressions = {
        31: /^(0(?:[1,3,5,7,8])|1(?:[0,2])([\/])(0(?:[1-9])|1(?:[0-9])|2(?:[0-9])|3(?:[0-1]))([\/]))(\d{4})$/,
        29: /^((02)([\/])(0(?:[1-9])|1(?:[0-9])|2(?:[0-9]))([\/]))(\d{4})$/,
        30: /^((0(?:[4,6,9])|11)([\/])(0(?:[1-9])|1(?:[0-9])|2(?:[0-9])|30)([\/]))(\d{4})$/
    };

    let valid = false;

    for(ex in expressions) {
        if(date.match(expressions[ex])) {
            valid = true;
        }
    }

    return valid;
}
